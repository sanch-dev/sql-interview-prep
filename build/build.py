#!/usr/bin/env python3
"""Validate every question against SQLite and emit docs/js/questions.js.

Each question's schema is loaded into a fresh in-memory database and its
reference solution is executed. The build fails loudly if any schema or
solution has an error, so the published question bank is always runnable.
"""
import json
import sqlite3
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))
from q_easy import EASY
from q_medium import MEDIUM
from q_hard import HARD
from q_more import EASY_MORE, MEDIUM_MORE, HARD_MORE
from q_interview_patterns import INTERVIEW_PATTERNS
from q_external_sources import EXTERNAL_SOURCES
from q_sqlzoo_curated import SQLZOO_PROBLEMS
from q_debug_challenges import DEBUG_CHALLENGES
from q_expansion import EXPANSION_MEDIUM, EXPANSION_HARD

ALL = EASY + EASY_MORE + MEDIUM + MEDIUM_MORE + HARD + HARD_MORE + INTERVIEW_PATTERNS + EXTERNAL_SOURCES + SQLZOO_PROBLEMS + DEBUG_CHALLENGES + EXPANSION_MEDIUM + EXPANSION_HARD
OUT = Path(__file__).parent.parent / "docs" / "js" / "questions.js"


def run_question(q):
    db = sqlite3.connect(":memory:")
    try:
        db.executescript(q["schema"])
        cur = db.execute(q["solution"])
        cols = [d[0] for d in cur.description] if cur.description else []
        rows = cur.fetchall()
        return cols, rows
    finally:
        db.close()


def main():
    verbose = "-v" in sys.argv
    failures = 0
    ids = set()
    print(f"Validating {len(ALL)} questions against SQLite {sqlite3.sqlite_version} ...\n")
    for q in ALL:
        qid = q["id"]
        if qid in ids:
            print(f"  FAIL {qid} duplicate id")
            failures += 1
            continue
        ids.add(qid)
        if q.get("tsql"):
            print(f"  SKIP {qid} [{q['difficulty']:<6}] {q['title']} (T-SQL — skipping SQLite validation)")
            continue
        try:
            cols, rows = run_question(q)
            if not rows:
                print(f"  FAIL {qid} {q['title']}: solution returned 0 rows")
                failures += 1
                continue
            print(f"  OK {qid} [{q['difficulty']:<6}] {q['title']} -> {len(rows)} row(s), cols: {cols}")
            if verbose:
                for r in rows:
                    print(f"      {r}")
        except sqlite3.Error as e:
            print(f"  FAIL {qid} {q['title']}: {e}")
            failures += 1

    if failures:
        print(f"\nBUILD FAILED: {failures} question(s) broken.")
        sys.exit(1)

    payload = json.dumps(ALL, indent=None, ensure_ascii=False)
    content = (
        "// Generated by build/build.py - do not edit by hand.\n"
        "window.QUESTIONS = " + payload + ";\n"
    )
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(content, encoding="utf-8")

    # Also write to frontend/public/ if the directory exists (local dev convenience)
    frontend_out = Path(__file__).parent.parent / "frontend" / "public" / "questions.js"
    if frontend_out.parent.exists():
        frontend_out.write_text(content, encoding="utf-8")
        print(f"  also wrote {frontend_out}")

    # Emit questions.json for both the standalone api/ server and Vercel serverless functions
    api_payload = [
        {"id": q["id"], "schema": q["schema"], "solution": q["solution"],
         "order_matters": q.get("order_matters", False), "tsql": q.get("tsql", False)}
        for q in ALL
    ]
    api_json = json.dumps(api_payload, indent=2, ensure_ascii=False)

    api_out = Path(__file__).parent.parent / "api" / "questions.json"
    api_out.parent.mkdir(parents=True, exist_ok=True)
    api_out.write_text(api_json, encoding="utf-8")
    print(f"  also wrote {api_out}")

    vercel_api_out = Path(__file__).parent.parent / "frontend" / "api" / "questions.json"
    if vercel_api_out.parent.exists():
        vercel_api_out.write_text(api_json, encoding="utf-8")
        print(f"  also wrote {vercel_api_out}")

    counts = {}
    for q in ALL:
        counts[q["difficulty"]] = counts.get(q["difficulty"], 0) + 1
    print(f"\nBUILD OK: wrote {OUT} ({len(ALL)} questions: {counts})")


if __name__ == "__main__":
    main()
